//! Built-in palettes for Director 6 (Windows + Mac)
//! and CLUT (Color Lookup Table) parser.
//!
//! Palette data sourced from ShockwaveParser.py (mulle.js project).

/// Parse a Director CLUT chunk into a palette (Vec of [R, G, B] entries)
/// Parse a Director CLUT chunk into a palette (Vec of [R, G, B] entries)
pub fn parse_clut(data: &[u8]) -> Vec<[u8; 3]> {
    let num = data.len() / 6;
    let mut colors = Vec::with_capacity(num);
    for i in 0..num {
        let off = i * 6;
        // 6 bytes per entry: R_hi, R_lo, G_hi, G_lo, B_hi, B_lo
        // Only high byte is significant
        let r = data[off];
        let g = data[off + 2];
        let b = data[off + 4];
        colors.push([r, g, b]);
    }
    // Director stores CLUT palettes in reversed order
    colors.reverse();
    colors
}

/// Director 6 Windows system palette (256 colors)
///
/// Stored as [B, G, R, 0] per entry, reordered to [R, G, B] here.
pub fn windows_palette() -> Vec<[u8; 3]> {
    PALETTE_WIN
        .chunks(4)
        .map(|bgra| [bgra[2], bgra[1], bgra[0]])
        .collect()
}

/// Director 6 Mac system palette (256 colors)
pub fn mac_palette() -> Vec<[u8; 3]> {
    PALETTE_MAC
        .chunks(4)
        .map(|bgra| [bgra[2], bgra[1], bgra[0]])
        .collect()
}

